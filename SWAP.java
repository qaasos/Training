
package FF_JAVA;

public class SWAP {

	public static void main(String[] args)
	{
		System.out.println("Hello Java and Selenium");

		int i = 10;
		int t = 20;
		int c;
		c = t;
		t = i;
		i = c;
		System.out.println("After Swapping");
		System.out.println("Value of i = "+ i);
		System.out.println("Value of t = "+ t);


	}

}
